$(document).ready(function(){

  $("#contact").click(hide)
  function hide (){
    $("#contact-info").fadeToggle("slow")
  }


  $("#projects").click(hides)
  function hides (){
    $("#projects-info").slideToggle()
  }

  $("#about").click(hidess)
  function hidess (){
    $("#about-info").fadeToggle("slow")
  }





});
